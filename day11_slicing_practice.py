# Day 11 - Slicing Practice

text = "PYTHON"
print("String:", text)
print("First 4 Letters:", text[:4])
print("Reverse:", text[::-1])

numbers = [10, 20, 30, 40, 50]
print("List Slice:", numbers[1:4])

marks = (80, 90, 100, 110, 120)
print("Tuple Slice:", marks[2:])



